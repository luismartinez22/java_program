    public boolean comprobarCampos(String comp) {
        if (comp.equals("todos")) {
            try {
                double valorDolar = Double.parseDouble(txtvalorDolar.getText());
                double valorGasOil = Double.parseDouble(txtvalorGasOil.getText());
                int configFletes = Integer.parseInt(txtconfigFletes.getText());
                if (valorDolar == 0 || valorGasOil == 0 || configFletes == 0) {
                    return false;
                }
            } catch (Exception e) {
                return false;
            }
        }
        if (comp.equals("todos") || comp.equals("basededatoslocal")) {
            if (!cbTpoConexionLocal.getSelectedItem().toString().equals("ODBC")) {
                if (txtUserLocal.getText().equals("") || txtBDLocal.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Faltaron ingresar datos obligatorios", "Error",
                            JOptionPane.INFORMATION_MESSAGE);
                    return false;
                }
            }
            if (!testConLocal()) {
                return false;
            }

        } else if (comp.equals("todos") || comp.equals("basededatosweb")) {
            if (jCheckBox1.isSelected()) {
                if (txtUrlWeb.getText().equals("") || txtUserWeb.getText().equals("") || txtBDWeb.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "Faltaron ingresar datos obligatorios", "Error",
                            JOptionPane.INFORMATION_MESSAGE);
                    return false;
                }
                if (!testConWeb()) {
                    return false;
                }
            }
        }
        return true;
    }

    public boolean testConLocal() {
        Connection conLocal = null;
        String tpo = cbTpoConexionLocal.getSelectedItem().toString();
        try {
            if (conLocal == null || conLocal.isClosed()) {
                if (tpo.equals("ODBC")) {
                    Class.forName("sun.jdbc.odbc.JdbcOdbcDriver");
                    if (/*WORATransportes.IsTerminal()*/false) {
                        conLocal = DriverManager.getConnection("jdbc:odbc:bd2Terminal");
                    } else {
                        conLocal = DriverManager.getConnection("jdbc:odbc:bd2");
                    }
                } else if (tpo.equals("Manual")) {
                    String user = txtUserLocal.getText();
                    String password = txtPassLocal.getText();
                    String bd = txtBDLocal.getText();
                    String url = "jdbc:mysql://127.0.0.1/" + bd;
                    Class.forName("com.mysql.jdbc.Driver");
                    conLocal = DriverManager.getConnection(url, user, password);
                }
            }
        } catch (Exception e) {
            conLocal = null;
        }
        if (conLocal != null) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Error en la conexion", ":-(",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        }
    }

    public boolean testConWeb() {
        Connection conWeb = null;
        try {
            if (conWeb == null || conWeb.isClosed()) {
                if (jCheckBox1.isSelected()) {
                    String user = txtUserWeb.getText();
                    String password = txtPassWeb.getText();
                    String bd = txtBDWeb.getText();
                    String url = "jdbc:mysql://" + txtUrlWeb.getText() + "/" + bd;
                    Class.forName("com.mysql.jdbc.Driver");
                    conWeb = DriverManager.getConnection(url, user, password);
                }
            }
        } catch (Exception e) {
            conWeb = null;
        }
        if (conWeb == null) {
            JOptionPane.showMessageDialog(null, "Error en la conexion", ":-(",
                    JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
