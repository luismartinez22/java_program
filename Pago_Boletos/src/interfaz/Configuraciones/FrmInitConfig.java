    private void txtvalorDolarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtvalorDolarKeyTyped
        char caracter = evt.getKeyChar();
        String text = txtvalorDolar.getText();
        if (caracter == '.') {
            if (text.equals("") || text.contains(".")) {
                evt.consume();
            }
        } else if (((caracter < '0')
                || (caracter > '9'))
                && (caracter != evt.VK_BACK_SPACE)) {
            evt.consume();
        }
        if (text.length() > 3) {
            evt.consume();
        }
    }//GEN-LAST:event_txtvalorDolarKeyTyped

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        if (comprobarCampos("todos")) {
            double valorDolar = Double.parseDouble(txtvalorDolar.getText());
            double valorGasOil = Double.parseDouble(txtvalorGasOil.getText());
            int configFletes = Integer.parseInt(txtconfigFletes.getText());
            Configuracion.getInstance().modificarConfiguraciones(valorDolar, valorGasOil, configFletes,
                    cbTpoConexionLocal.getSelectedItem().toString(), txtUserLocal.getText(), txtPassLocal.getText(), txtBDLocal.getText(),
                    jCheckBox1.isSelected(), txtUrlWeb.getText(), txtUserWeb.getText(), txtPassWeb.getText(), txtBDWeb.getText());
            if (Configuracion.getInstance().modificar()) {
                inicio = true;
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Faltaron ingresar datos obligatorios", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

